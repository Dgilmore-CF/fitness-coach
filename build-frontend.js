import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * Bundles the frontend JavaScript for embedding in the Cloudflare Worker.
 *
 * During the refactor this script combines:
 *   1. Legacy monolithic `public/app.js` (shrinking over time)
 *   2. Vite-built modular bundle from `frontend-dist/` (growing over time)
 *
 * Output is written to `src/frontend.js` as a template-literal export so the
 * Worker can inline it into the HTML response.
 *
 * When the refactor is complete this script will be replaced by Cloudflare
 * Static Assets bindings and deleted.
 */

const PROJECT_ROOT = __dirname;
const LEGACY_APP = path.join(PROJECT_ROOT, 'public/app.js');
const VITE_DIST = path.join(PROJECT_ROOT, 'frontend-dist');
const OUTPUT = path.join(PROJECT_ROOT, 'src/frontend.js');

function escapeForTemplateLiteral(source) {
  return source
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$');
}

function readVitebundle() {
  if (!fs.existsSync(VITE_DIST)) {
    return { js: '', css: '' };
  }

  const assetsDir = path.join(VITE_DIST, 'assets');
  if (!fs.existsSync(assetsDir)) {
    return { js: '', css: '' };
  }

  const files = fs.readdirSync(assetsDir);
  const jsFile = files.find((f) => f.endsWith('.js'));
  const cssFile = files.find((f) => f.endsWith('.css'));

  return {
    js: jsFile ? fs.readFileSync(path.join(assetsDir, jsFile), 'utf8') : '',
    css: cssFile ? fs.readFileSync(path.join(assetsDir, cssFile), 'utf8') : ''
  };
}

const legacyJS = fs.existsSync(LEGACY_APP) ? fs.readFileSync(LEGACY_APP, 'utf8') : '';
const { js: modularJS, css: modularCSS } = readVitebundle();

// Combined bundle: Vite modules first (they may define utilities the legacy code uses),
// then legacy code. Wrap each in an IIFE so they don't clobber each other's scope.
const combined = [
  modularJS ? `/* === Vite-built modules === */\n${modularJS}` : '',
  legacyJS ? `/* === Legacy app.js === */\n${legacyJS}` : ''
].filter(Boolean).join('\n\n');

const cssBlock = modularCSS
  ? `/* === Modular CSS (injected via <style>) === */\n${modularCSS}`
  : '';

const output = `// Auto-generated by build-frontend.js — do not edit manually
export const frontendJS = \`${escapeForTemplateLiteral(combined)}\`;
export const frontendCSS = \`${escapeForTemplateLiteral(cssBlock)}\`;
export default frontendJS;
`;

fs.writeFileSync(OUTPUT, output);

const stats = {
  legacy: (legacyJS.length / 1024).toFixed(1),
  modular: (modularJS.length / 1024).toFixed(1),
  css: (modularCSS.length / 1024).toFixed(1),
  total: (combined.length / 1024).toFixed(1)
};

console.log('✓ Frontend bundled');
console.log(`  Legacy app.js:   ${stats.legacy} KB`);
console.log(`  Modular JS:      ${stats.modular} KB`);
console.log(`  Modular CSS:     ${stats.css} KB`);
console.log(`  Total bundle:    ${stats.total} KB`);
